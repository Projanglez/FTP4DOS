/* --------------------------------------------------------------------- */
/* State / init                                                           */
/* --------------------------------------------------------------------- */

static unsigned              g_cp  = 0;        /* 0 = not initialized    */
static const unsigned short *g_tab = cp437_hi;

/* Active DOS codepage via INT 21h AX=6601h (DOS 3.3+). */
static unsigned detect_codepage(void)
{
    union REGS r;
    r.w.ax = 0x6601;
    intdos(&r, &r);
    if (r.w.cflag || r.w.bx == 0) return 437;
    return r.w.bx;
}

void cpmap_init(unsigned codepage)
{
    g_cp = codepage ? codepage : detect_codepage();
    switch (g_cp) {
        case 850:
        case 858: g_tab = cp850_hi; break;   /* 858 = 850 with euro sign */
        case 866: g_tab = cp866_hi; break;
        default:  g_tab = cp437_hi; break;   /* incl. 437 */
    }
}

static void ensure_init(void)
{
    if (g_cp == 0) cpmap_init(0);
}

unsigned cpmap_codepage(void)
{
    ensure_init();
    return g_cp;
}

/* --------------------------------------------------------------------- */
/* UTF-8 decoding                                                         */
/* --------------------------------------------------------------------- */

/* Decode one UTF-8 sequence at *p. On success: returns the codepoint and
 * advances *p past the sequence. On an invalid sequence: returns -1 and
 * leaves *p unchanged. ASCII bytes are returned as-is. */
static long utf8_decode(const unsigned char **p)
{
    const unsigned char *s = *p;
    unsigned char b = s[0];
    long cp;
    int  n, i;

    if (b < 0x80) { (*p)++; return (long)b; }
    if (b >= 0xC2 && b <= 0xDF) { n = 1; cp = b & 0x1F; }
    else if (b >= 0xE0 && b <= 0xEF) { n = 2; cp = b & 0x0F; }
    else if (b >= 0xF0 && b <= 0xF4) { n = 3; cp = b & 0x07; }
    else return -1;                      /* continuation byte / overlong lead */

    for (i = 1; i <= n; i++) {
        if ((s[i] & 0xC0) != 0x80) return -1;
        cp = (cp << 6) | (s[i] & 0x3F);
    }
    /* Reject overlong 3/4-byte forms and surrogates. */
    if (n == 2 && cp < 0x800) return -1;
    if (n == 3 && (cp < 0x10000L || cp > 0x10FFFFL)) return -1;
    if (cp >= 0xD800L && cp <= 0xDFFFL) return -1;

    *p += n + 1;
    return cp;
}

int cpmap_is_utf8(const char *s)
{
    const unsigned char *p = (const unsigned char *)s;
    int multi = 0;
    while (*p) {
        if (*p < 0x80) { p++; continue; }
        if (utf8_decode(&p) < 0) return 0;
        multi = 1;
    }
    return multi;
}

/* --------------------------------------------------------------------- */
/* Conversion                                                             */
/* --------------------------------------------------------------------- */

/* Unicode codepoint -> codepage byte, or 0 if unmappable. */
static unsigned char uni_to_cp(long cp)
{
    int i;
    if (cp < 0x80) return (unsigned char)cp;
    for (i = 0; i < 128; i++)
        if ((long)g_tab[i] == cp) return (unsigned char)(0x80 + i);
    return 0;
}

void cpmap_utf8_to_cp(const char *src, char *dst, int dstsz)
{
    const unsigned char *p = (const unsigned char *)src;
    int  o = 0;
    long cp;

    ensure_init();
    while (*p && o < dstsz - 1) {
        cp = utf8_decode(&p);
        if (cp < 0) {                     /* invalid byte: keep it verbatim */
            dst[o++] = (char)*p++;
            continue;
        }
        if (cp < 0x80) {
            dst[o++] = (char)cp;
        } else {
            unsigned char c = uni_to_cp(cp);
            dst[o++] = c ? (char)c : '_';
        }
    }
    dst[o] = '\0';
}

void cpmap_cp_to_utf8(const char *src, char *dst, int dstsz)
{
    const unsigned char *p = (const unsigned char *)src;
    int o = 0;

    ensure_init();
    while (*p && o < dstsz - 1) {
        unsigned char b = *p++;
        if (b < 0x80) {
            dst[o++] = (char)b;
        } else {
            unsigned short u = g_tab[b - 0x80];
            if (u < 0x800) {
                if (o >= dstsz - 2) break;
                dst[o++] = (char)(0xC0 | (u >> 6));
                dst[o++] = (char)(0x80 | (u & 0x3F));
            } else {
                if (o >= dstsz - 3) break;
                dst[o++] = (char)(0xE0 | (u >> 12));
                dst[o++] = (char)(0x80 | ((u >> 6) & 0x3F));
                dst[o++] = (char)(0x80 | (u & 0x3F));
            }
        }
    }
    dst[o] = '\0';
}
